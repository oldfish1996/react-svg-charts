export function calcTextWidth(text: string = '', fontSize: number = 10) {
  if (!text) {
    return 0;
  }
  return text.split('').reduce((pre, cur) => {
    const ratio = DEFAULT_TEXT_WIDTH_MAP[cur];
    return (ratio ? ratio * fontSize : fontSize) + pre;
  }, 0);
}
